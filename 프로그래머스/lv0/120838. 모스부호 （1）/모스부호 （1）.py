def solution(letter):
    morse = [".-","-...","-.-.","-..",".","..-.","--.","....","..",".---","-.-",".-..","--","-.","---",".--.","--.-",".-.","...","-","..-","...-",".--","-..-","-.--","--.."]
    ab = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
    
    answer = ''
    n = ''
    a = 0
    
    for i in letter.split(" ") :
        n += i
        a = morse.index(n)
        if (a >= 0) :
            n = ''
            answer += ab[a]
    return answer